CK_RV
token_specific_sha_update(	DIGEST_CONTEXT	*ctx,
				CK_BYTE		*in_data,
				CK_ULONG	in_data_len )
{
	unsigned int	rc, i, fill_size = 0;
	oc_sha1_ctx	*oc_sha_ctx = (oc_sha1_ctx *)ctx->context;
	SHA_CONTEXT	*ica_sha_ctx = (SHA_CONTEXT *)oc_sha_ctx->dev_ctx;
	
	if( !ctx )
		return CKR_OPERATION_NOT_INITIALIZED;
	
	if( !in_data )
		return CKR_FUNCTION_FAILED;
	
	if( ctx->multi == TRUE ){
		if (oc_sha_ctx->tail_len == 64) {
                        /* Submit the filled out save buffer */
                        if( icaSha1(    adapter_handle,
                                        ica_sha_ctx->runningLength == 0 ? SHA_MSG_PART_FIRST : SHA_MSG_PART_MIDDLE,
                                        64, oc_sha_ctx->tail,
                                        LENGTH_SHA_CONTEXT, ica_sha_ctx,
                                        &oc_sha_ctx->hash_len, oc_sha_ctx->hash))
                                return CKR_FUNCTION_FAILED;
                        oc_sha_ctx->tail_len = 0;
                }

		/* libICA (and SHA1) demands that if this is a PART_FIRST or a 
		 * PART_MIDDLE operation, the amount of data passed in
		 * must be a multiple of 64 bytes. - KEY
		 */
		if( ica_sha_ctx->runningLength == 0 && 
			oc_sha_ctx->tail_len == 0) {
			oc_sha_ctx->message_part = SHA_MSG_PART_FIRST;

                        /* Just copying the last <64 bytes will not work in the case
                         * of a user who SHA's a large chunk of data in 64 byte
                         * pieces because we need to cache the last 64 bytes so that
                         * we're not stuck with 0 bytes when the MSG_PART_FINAL
                         * comes in. - KEY
                         */
                        if (!(in_data_len % 64)) {
                                oc_sha_ctx->tail_len = 64;
                                memcpy(oc_sha_ctx->tail, in_data + in_data_len - 64, 64);
                                in_data_len -= 64;
                        } else
                                oc_sha_ctx->tail_len = in_data_len & 0x3f;
		
			if(oc_sha_ctx->tail_len < 64) {
				in_data_len &= ~0x3f;	
				memcpy(oc_sha_ctx->tail, in_data + in_data_len, oc_sha_ctx->tail_len);
			}
		}
		else if( ica_sha_ctx->runningLength == 0 &&
				oc_sha_ctx->tail_len > 0 ) {

			/* Here we need to fill out the temporary tail buffer until
			 * it has 64 bytes in it, then call icaSha1 on that buffer.
			 * If there weren't enough bytes passed in to fill it out,
			 * just copy in what we can and return success without calling
			 * icaSha1. - KEY
			 */

			fill_size = 64 - oc_sha_ctx->tail_len;
			if(fill_size < in_data_len) {
				memcpy(oc_sha_ctx->tail + oc_sha_ctx->tail_len, in_data, fill_size);
			
				/* Submit the filled out save buffer */
				if( icaSha1(    adapter_handle, SHA_MSG_PART_FIRST,
						64, oc_sha_ctx->tail,
						LENGTH_SHA_CONTEXT, ica_sha_ctx,
						&oc_sha_ctx->hash_len, oc_sha_ctx->hash))
					return CKR_FUNCTION_FAILED;
			} else {
				memcpy(oc_sha_ctx->tail + oc_sha_ctx->tail_len,
					in_data, in_data_len);
				oc_sha_ctx->tail_len += in_data_len;

				return CKR_OK;
			}
			
                        /* We had to use 'fill_size' bytes from in_data to fill out the
                         * empty part of save data, so adjust in_data_len
                         */
                        in_data_len -= fill_size;

                        oc_sha_ctx->tail_len = in_data_len & 0x3f;
                        if(oc_sha_ctx->tail_len) {
                                memcpy(oc_sha_ctx->tail, 
					in_data + fill_size, 
					oc_sha_ctx->tail_len);
                                in_data_len &= ~0x3f;
                        }
		}
		else if( ica_sha_ctx->runningLength > 0 ) {
			oc_sha_ctx->message_part = SHA_MSG_PART_MIDDLE;

			if(oc_sha_ctx->tail_len) {
	                        fill_size = 64 - oc_sha_ctx->tail_len;
        	                if(fill_size < in_data_len) {
                	                memcpy(oc_sha_ctx->tail + oc_sha_ctx->tail_len, 
							in_data, fill_size);

                        	        /* Submit the filled out save buffer */
                                	if( icaSha1(    adapter_handle, oc_sha_ctx->message_part,
                                        	        64, oc_sha_ctx->tail,
                                                	LENGTH_SHA_CONTEXT, ica_sha_ctx,
	                                                &oc_sha_ctx->hash_len, oc_sha_ctx->hash))
        	                                return CKR_FUNCTION_FAILED;
                	        } else {
                        	        memcpy(oc_sha_ctx->tail + oc_sha_ctx->tail_len,
                                	        in_data, in_data_len);
	                                oc_sha_ctx->tail_len += in_data_len;
	
        	                        return CKR_OK;
                	        }

				/* We had to use some of the data from in_data to fill out the
				 * empty part of save data, so adjust in_data_len
				 */
				in_data_len -= fill_size;
			
				oc_sha_ctx->tail_len = in_data_len & 0x3f;
				if(oc_sha_ctx->tail_len) {
					memcpy(oc_sha_ctx->tail, in_data + fill_size, 
							oc_sha_ctx->tail_len);
					in_data_len &= ~0x3f;	
				}
			} else {
				/* This is the odd case, where we need to go ahead and
				 * send the first X * 64 byte chunks in to be processed
				 * and copy the last <64 byte area into the tail. -KEY
				 */
                                /* Just copying the last <64 bytes will not work in the case
                                 * of a user who SHA's a large chunk of data in 64 byte
                                 * pieces because we need to cache the last 64 bytes so that
                                 * we're not stuck with 0 bytes when the MSG_PART_FINAL
                                 * comes in. - KEY
                                 */
                                if (!(in_data_len % 64)) {
                                        oc_sha_ctx->tail_len = 64;
                                        memcpy(oc_sha_ctx->tail, in_data + in_data_len - 64, 64);
                                        in_data_len -= 64;
                                } else
                                        oc_sha_ctx->tail_len = in_data_len & 0x3f;

				if( oc_sha_ctx->tail_len < 64) {
					in_data_len &= ~0x3f;
					memcpy(oc_sha_ctx->tail, in_data + in_data_len,
							oc_sha_ctx->tail_len);
				}
			}
		}
	} else {
		/* ctx->multi == FALSE, but we've run previously. That's
		 * our signal that this is the last part -KEY
		 */
		if( ica_sha_ctx->runningLength > 0 )
			oc_sha_ctx->message_part = SHA_MSG_PART_FINAL;
		else
			oc_sha_ctx->message_part = SHA_MSG_PART_ONLY;
	}
	
	if( in_data_len || oc_sha_ctx->message_part == SHA_MSG_PART_FINAL ) {
		if( icaSha1(	adapter_handle, oc_sha_ctx->message_part,
				in_data_len, in_data + fill_size,
				LENGTH_SHA_CONTEXT, ica_sha_ctx,
				&oc_sha_ctx->hash_len, oc_sha_ctx->hash))
			return CKR_FUNCTION_FAILED;
	}
	
	return CKR_OK;
}

CK_RV token_specific_sha2_update(DIGEST_CONTEXT *ctx, CK_BYTE *in_data,
				 CK_ULONG in_data_len)
{
	unsigned int rc, i, fill_size = 0;
	CK_RV rv = CKR_OK;
/*	syslog(LOG_ERR, "%s: Enter\n", __FUNCTION__); */
	oc_sha2_ctx *oc_sha256_ctx = (oc_sha2_ctx *)ctx->context;
	SHA256_CONTEXT *ica_sha2_ctx=(SHA256_CONTEXT *)oc_sha256_ctx->dev_ctx;
	if (!ctx) {
/*		syslog(LOG_ERR, "%s: NULL context\n", __FUNCTION__); */
		rv = CKR_OPERATION_NOT_INITIALIZED;
		goto out;
	}
	if (!in_data) {
/*		syslog(LOG_ERR, "%s: NULL in_data\n", __FUNCTION__); */
		rv = CKR_FUNCTION_FAILED;
		goto out;
	}
	if (ctx->multi == TRUE) {
		if (oc_sha256_ctx->tail_len == 64) {
			int rc;
			/* Submit the filled out save buffer */
                        if (rc = icaSha256(adapter_handle, 
					   (ica_sha2_ctx->runningLength == 0 
					    ? SHA_MSG_PART_FIRST 
					    : SHA_MSG_PART_MIDDLE),
					   64, oc_sha256_ctx->tail,
					   LENGTH_SHA256_CONTEXT, ica_sha2_ctx,
					   &oc_sha256_ctx->hash_len,
					   oc_sha256_ctx->hash)) {
/*				syslog(LOG_ERR, "%s: [1]libICA unhappy about "
				       "something; return code = [%d]\n",
				       __FUNCTION__, rc); */
				if (rc == ENODEV) {
/*					syslog(LOG_ERR, "%s: libICA "
					       "reports that we don't "
					       "have any hardware "
					       "SHA256 support.\n",
					       __FUNCTION__); */
				}
				rv = CKR_FUNCTION_FAILED;
				goto out;
			}
			oc_sha256_ctx->tail_len = 0;
		}
		/* libICA and SHA256 demand that if this is a
		 * PART_FIRST or a PART_MIDDLE operation, the amount
		 * of data passed in must be a multiple of 64 bytes. -
		 * KEY
		 */
		if (ica_sha2_ctx->runningLength == 0 && 
		    oc_sha256_ctx->tail_len == 0) {
			oc_sha256_ctx->message_part = SHA_MSG_PART_FIRST;
			/* Just copying the last <64 bytes will not
			 * work in the case of a user who SHA's a
			 * large chunk of data in 64 byte pieces
			 * because we need to cache the last 64 bytes
			 * so that we're not stuck with 0 bytes when
			 * the MSG_PART_FINAL comes in. - KEY
			 */
			if (!(in_data_len % 64)) {
				oc_sha256_ctx->tail_len = 64;
				memcpy(oc_sha256_ctx->tail,
				       (in_data + in_data_len - 64), 64);
				in_data_len -= 64;
			} else {
				oc_sha256_ctx->tail_len = in_data_len & 0x3f;
			}
			if (oc_sha256_ctx->tail_len < 64) {
				in_data_len &= ~0x3f;	
				memcpy(oc_sha256_ctx->tail,
				       (in_data + in_data_len),
				       oc_sha256_ctx->tail_len);
			}
		} else if (ica_sha2_ctx->runningLength == 0 &&
			   oc_sha256_ctx->tail_len > 0) {
			/* Here we need to fill out the temporary tail
			 * buffer until it has 64 bytes in it, then
			 * call icaSha256 on that buffer.  If there
			 * weren't enough bytes passed in to fill it
			 * out, just copy in what we can and return
			 * success without calling icaSha1. - KEY
			 */
			fill_size = 64 - oc_sha256_ctx->tail_len;
			if (fill_size < in_data_len) {
				int rc;
				memcpy((oc_sha256_ctx->tail
					+ oc_sha256_ctx->tail_len),
				       in_data, fill_size);
				/* Submit the filled out save buffer */
				if (rc = icaSha256(adapter_handle,
						   (unsigned int)SHA_MSG_PART_FIRST,
						   (unsigned int)64,
						   oc_sha256_ctx->tail,
						   (unsigned int)
						   LENGTH_SHA256_CONTEXT, 
						   ica_sha2_ctx, 
						   &oc_sha256_ctx->hash_len,
						   oc_sha256_ctx->hash)) {
/*					syslog(LOG_ERR, "%s: [2]libICA unhappy"
					       " about something; return code "
					       "= [%d]\n",
					       __FUNCTION__, rc); */
					if (rc == ENODEV) {
/*						syslog(LOG_ERR, "%s: libICA "
						       "reports that we don't "
						       "have any hardware "
						       "SHA256 support.\n",
						       __FUNCTION__); */
					}
					rv = CKR_FUNCTION_FAILED;
					goto out;
				}
			} else {
				memcpy((oc_sha256_ctx->tail
					+ oc_sha256_ctx->tail_len),
				       in_data, in_data_len);
				oc_sha256_ctx->tail_len += in_data_len;
				goto out;
			}
                        /* We had to use 'fill_size' bytes from
                         * in_data to fill out the empty part of save
                         * data, so adjust in_data_len
                         */
                        in_data_len -= fill_size;
                        oc_sha256_ctx->tail_len = in_data_len & 0x3f;
                        if (oc_sha256_ctx->tail_len) {
                                memcpy(oc_sha256_ctx->tail, in_data + fill_size,
				       oc_sha256_ctx->tail_len);
                                in_data_len &= ~0x3f;
                        }
		} else if (ica_sha2_ctx->runningLength > 0) {
			oc_sha256_ctx->message_part = SHA_MSG_PART_MIDDLE;
			if(oc_sha256_ctx->tail_len) {
	                        fill_size = (64 - oc_sha256_ctx->tail_len);
        	                if (fill_size < in_data_len) {
					int rc;
                	                memcpy(oc_sha256_ctx->tail
					       + oc_sha256_ctx->tail_len, 
					       in_data, fill_size);
                        	        /* Submit the filled out save
					 * buffer */
					rc = icaSha256(adapter_handle,
						       (unsigned int)
						       oc_sha256_ctx->message_part,
						       (unsigned int)64,
						       oc_sha256_ctx->tail,
						       (unsigned int)
						       LENGTH_SHA256_CONTEXT,
						       ica_sha2_ctx,
						       &oc_sha256_ctx->hash_len,
						       oc_sha256_ctx->hash);
                                	if (rc) {
/*						syslog(LOG_ERR,
						       "%s: [3]libICA unhappy "
						       "about something; "
						       "return code = [%d]\n",
						       __FUNCTION__,
						       rc); */
						rv = CKR_FUNCTION_FAILED;
						goto out;
					}
                	        } else {
                        	        memcpy((oc_sha256_ctx->tail
						+ oc_sha256_ctx->tail_len),
					       in_data, in_data_len);
	                                oc_sha256_ctx->tail_len += in_data_len;
					goto out;
                	        }
				/* We had to use some of the data from
				 * in_data to fill out the empty part
				 * of save data, so adjust in_data_len
				 */
				in_data_len -= fill_size;
				oc_sha256_ctx->tail_len = in_data_len & 0x3f;
				if(oc_sha256_ctx->tail_len) {
					memcpy(oc_sha256_ctx->tail,
					       (in_data + fill_size),
					       oc_sha256_ctx->tail_len);
					in_data_len &= ~0x3f;	
				}
			} else {
				/* This is the odd case, where we need
				 * to go ahead and send the first X *
				 * 64 byte chunks in to be processed
				 * and copy the last <64 byte area
				 * into the tail. -KEY
				 */
				/* Just copying the last <64 bytes
				 * will not work in the case of a user
				 * who SHA's a large chunk of data in
				 * 64 byte pieces because we need to
				 * cache the last 64 bytes so that
				 * we're not stuck with 0 bytes when
				 * the MSG_PART_FINAL comes in. - KEY
				 */
                        	if (!(in_data_len % 64)) {
                                	oc_sha256_ctx->tail_len = 64;
	                                memcpy(oc_sha256_ctx->tail,
					       (in_data + in_data_len - 64),
					       64);
        	                        in_data_len -= 64;
                	        } else {
                        	        oc_sha256_ctx->tail_len =
						in_data_len & 0x3f;
				}
				if (oc_sha256_ctx->tail_len < 64) {
					in_data_len &= ~0x3f;
					memcpy(oc_sha256_ctx->tail,
					       (in_data + in_data_len),
					       oc_sha256_ctx->tail_len);
				}
			}
		}
	} else {
		/* ctx->multi == FALSE, but we've run
		 * previously. That's our signal that this is the last
		 * part -KEY
		 */
		if (ica_sha2_ctx->runningLength > 0) {
			oc_sha256_ctx->message_part = SHA_MSG_PART_FINAL;
		} else {
			oc_sha256_ctx->message_part = SHA_MSG_PART_ONLY;
		}
	}
	if (in_data_len 
	    || (oc_sha256_ctx->message_part == SHA_MSG_PART_FINAL)) {
		int rc;
		if (rc = icaSha256(adapter_handle,
				   (unsigned int)oc_sha256_ctx->message_part,
				   (unsigned int)in_data_len,
				   (in_data + fill_size),
				   (unsigned int)LENGTH_SHA256_CONTEXT,
				   ica_sha2_ctx,
				   &oc_sha256_ctx->hash_len,
				   oc_sha256_ctx->hash)) {
/*			syslog(LOG_ERR, "%s: [4]libICA unhappy about something"
			       "; return code = [%d]\n", __FUNCTION__,
			       rc); */
			rv = CKR_FUNCTION_FAILED;
			goto out;
		}
	}
 out:
/*	syslog(LOG_ERR, "%s: Exit; rv = [%d]\n", __FUNCTION__, rv); */
	return rv;
}

CK_RV
token_specific_sha_final(	DIGEST_CONTEXT	*ctx,
				CK_BYTE		*out_data,
				CK_ULONG	*out_data_len )
{
	CK_RV rv = CKR_OK;
	oc_sha1_ctx *oc_sha_ctx = (oc_sha1_ctx *)ctx->context;
	int copy_len = MIN(*out_data_len, LENGTH_SHA_HASH);
	
	if( !ctx )
		return CKR_OPERATION_NOT_INITIALIZED;
	
	if( !out_data || (*out_data_len < LENGTH_SHA_HASH) )
		return CKR_FUNCTION_FAILED;

	if( oc_sha_ctx->message_part != SHA_MSG_PART_FINAL &&
	    oc_sha_ctx->message_part != SHA_MSG_PART_ONLY) {
		/* Finalize the SHA operation; tell update that this multi-part
		 * operation is done. -KEY
		*/
		ctx->multi = FALSE;
		token_specific_sha_update(ctx, oc_sha_ctx->tail, oc_sha_ctx->tail_len);
	}
	
	memcpy(out_data, oc_sha_ctx->hash, copy_len);
	*out_data_len = copy_len;

	/* ctx->context is freed inside digest_mgr_cleanup - KEY */
	free(oc_sha_ctx->dev_ctx);
	return rv;
}

CK_RV token_specific_sha2_final(DIGEST_CONTEXT *ctx, CK_BYTE *out_data,
				CK_ULONG *out_data_len)
{
	CK_RV rv = CKR_OK;
	oc_sha2_ctx *oc_sha256_ctx = (oc_sha2_ctx *)ctx->context;
	int copy_len = MIN(*out_data_len, LENGTH_SHA256_HASH);
	if (!ctx) {
		return CKR_OPERATION_NOT_INITIALIZED;
	}
	if(!out_data || (*out_data_len < LENGTH_SHA256_HASH)) {
		return CKR_FUNCTION_FAILED;
	}
	if ((oc_sha256_ctx->message_part != SHA_MSG_PART_FINAL)
	    && (oc_sha256_ctx->message_part != SHA_MSG_PART_ONLY)) {
		/* Finalize the SHA operation; tell update that this
		 * multi-part operation is done. -KEY */
		ctx->multi = FALSE;
		token_specific_sha2_update(ctx, oc_sha256_ctx->tail,
					   oc_sha256_ctx->tail_len);
	}
	memcpy(out_data, oc_sha256_ctx->hash, copy_len);
	*out_data_len = copy_len;
	/* ctx->context is freed inside digest_mgr_cleanup - KEY */
	free(oc_sha256_ctx->dev_ctx);
	return rv;
}

